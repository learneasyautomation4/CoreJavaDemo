package day1_Variables_PrimitiveDataTypes_Comments.primitiveDataType;

public class PrimitiveDataType_FloatingNumbers {
    public static void main(String[] args) {
        //float -> double


        //float data types => 4 bytes -> 7 to 8 decimal digits
        float f1=85.1245f;
        System.out.println("Float data type value:" + f1);

        //double data  type
        //double -> 8 bytes and 15 decimal digits
        double doubleVarName=8536288596.5465451321987984651d;

        System.out.println("Double data type value: " + doubleVarName);

    }
}
